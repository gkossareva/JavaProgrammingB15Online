package day07;

public class RelationalOperatorWithVariable {
    public static void main(String[] args) {

        //relational operator is used to compare 2 oprands
        //and return true or false
        //anything action or false result
        //can be called boolean expression
        int myNumber = 10;
        System.out.println(myNumber > 5);
        System.out.println(myNumber < -19);
        System.out.println(myNumber >= 5);
        System.out.println(myNumber >= 8);
        System.out.println(myNumber == 5);
        System.out.println(myNumber >= 8);

        //= = is used t check for equality and very different from single=
        //difference between == and = same as difference between equal and equality in  daily English
        //!= is used to check for iquality ! in programming translaet to NOT
        //!= == ARE questions checking for equality or inequality
        System.out.println(myNumber == 5);
        System.out.println(myNumber != 10);
    }
}
