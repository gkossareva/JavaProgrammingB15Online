package day03;

public class StringDataType {
    public static void main(String[] args) {
        String name = "Galina";
        int age = 32;
        double height = 5.9;
        char myLastNameFirstChar = 'G';
        boolean isMarried = true;
        byte childrenCount = 2;
        String city = "Alexandria";

        System.out.println("My name is "+ name);
        System.out.println("My age is " + age);
        System.out.println("My height is "+ height);
        System.out.println("My last name first character is " + myLastNameFirstChar);
        System.out.println("I am married "+ isMarried);
        System.out.println("I have " + childrenCount+ " kids");
        System.out.println("I live in " + city);
        System.out.println("My name is "+ name+". My age is "+ age+ ". My height is "+ height+". My last name first character is "+myLastNameFirstChar+". I am married: "+ isMarried+". I have "+childrenCount + " kids."+ "I live in "+city+".");




    }
}
