package day04;

public class FahrenheitCelcius {
    public static void main(String[] args) {
        double celcius;
        double fahrenheit=28;
        celcius = (5.0/9)*(fahrenheit-32);
        //write a program that converts a fahrenheit degree to celcius using the formula celcius(5/9)(fahrenheit-32)
        System.out.println(fahrenheit+ "F equals to "+celcius+"C.");
    }
}
